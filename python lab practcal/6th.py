#  Q6. How to create variables in Python? 
# Integer variable
# Integer variable
age = 21

# Float variable
price = 99.99

# String variable
name = "Swastik"

# Boolean variable
is_student = True

# List variable
fruits = ["apple", "banana", "cherry"]

# Tuple variable
coordinates = (10, 20)

# Dictionary variable
student = {"name": "Swastik", "age": 21, "course": "Python"}

# Print outputs
print("Age:", age)
print("Price:", price)
print("Name:", name)
print("Is Student:", is_student)
print("Fruits:", fruits)
print("Coordinates:", coordinates)
print("Student Dictionary:", student)

