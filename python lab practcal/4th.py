#  Q4. Write a Python program to demonstrate the creation of variables and different data types. 

# Python program to demonstrate variable creation and different data types

# Integer
age = 25
print("Age (Integer):", age)

# Float
height = 5.9
print("Height (Float):", height)

# String
name = "Swastik"
print("Name (String):", name)

# Boolean
is_student = True
print("Is Student (Boolean):", is_student)

# List
fruits = ["apple", "banana", "cherry"]
print("Fruits (List):", fruits)

# Tuple
coordinates = (10.5, 20.7)
print("Coordinates (Tuple):", coordinates)

# Set
unique_numbers = {1, 2, 3, 2, 1}
print("Unique Numbers (Set):", unique_numbers)

# Dictionary
student = {
    "name": "Swastik",
    "age": 22,
    "course": "Python"
}
print("Student Details (Dictionary):", student)

# NoneType
nothing = None
print("Nothing (NoneType):", nothing)
