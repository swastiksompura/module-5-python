# Write a Python program to demonstrate string slicing. 

# Define a string
text = "Python Programming"

# Slicing examples
print("Original string:", text)

# Slice from index 0 to 5
print("text[0:6] ->", text[0:6])

# Slice from index 7 to end
print("text[7:] ->", text[7:])

# Slice from start to index 5
print("text[:6] ->", text[:6])

# Slice with step (every 2nd character)
print("text[::2] ->", text[::2])

# Reverse the string
print("text[::-1] ->", text[::-1])
